package pl.sda.mg.jav110.jzp.zad9;

public class MoveDirection {
    private final double x;
    private final double y;

    public MoveDirection(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getY() {
        return y;
    }

    public double getX() {
        return x;
    }
}
